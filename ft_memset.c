#include "libft.h"

void	ft_memset(void *ptr, int vlu, size_t cnt)
{
	unsigned char	*ptr2;

	ptr2 = (unsigned char *)ptr;
	while (cnt > 0)
	{
		*(ptr2++) = vlu;
		cnt--;
	}
}
